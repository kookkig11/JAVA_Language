package i06;

public class KeepBall {
    private String type;
    private String price;
    private String sell;
    private String value;

    public KeepBall(String type, String price, String sell, String value) {
        this.type = this.type;
        this.price = this.price;
        this.sell = this.sell;
        this.value = this.value;
    }

    public String getType() {
        return type;
    }

    public String getPrice() {
        return price;
    }

    public String getSell() {
        return sell;
    }

    public String getValue() {
        return value;
    }
}
